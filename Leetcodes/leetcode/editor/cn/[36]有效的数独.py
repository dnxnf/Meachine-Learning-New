class Solution:
    def isValidSudoku1(self, board: List[List[str]]) -> bool:
        dicRow = {}
        dicCol = {}
        dicBox = {}
        for i in range(9):
            for j in range(9):
                if board[i][j] == '.':
                    continue
                num = int(board[i][j])
                if num in dicRow.get(i, []) or num in dicCol.get(j, []) or num in dicBox.get(i // 3 * 3 + j // 3, []):
                    return False
                dicRow[i] = dicRow.get(i, []) + [num]
                dicCol[j] = dicCol.get(j, []) + [num]
                dicBox[i // 3 * 3 + j // 3] = dicBox.get(i // 3 * 3 + j // 3, []) + [num]
        return True



