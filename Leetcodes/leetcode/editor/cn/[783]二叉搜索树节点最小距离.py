# Definition for a binary tree node.
class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right
class Solution:
    def minDiffInBST(self, root: Optional[TreeNode]) -> int:
        path = []
        def dfs(node):
            if not node:
                return
            dfs(node.left)
            path.append(node.val)
            dfs(node.right)
        dfs(root)
        minn = float('inf')
        for i in range(len(path) - 1):
            minn = min(minn, path[i + 1] - path[i])
        return minn
